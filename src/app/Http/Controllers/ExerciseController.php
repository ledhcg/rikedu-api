    {
        $exercises = Exercise::where('student_id', $studentID)->get();
        if (!$exercises) {
            return $this->notFoundResponse('Exercises not found');
        }
        return $this->successResponse(
            new ExerciseCollection($exercises),
            'Exercises retrieved successfully'
        );
    }

