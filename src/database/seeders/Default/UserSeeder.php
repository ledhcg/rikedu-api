    public function convert_gender($gender, $lowerCase = false)
    {
        if ($gender == 'женский') {
            return $lowerCase ? strtolower('Female') : 'Female';
        }

        if ($gender == 'мужской') {
            return $lowerCase ? strtolower('Male') : 'Male';
        }

        return $lowerCase ? strtolower('Other') : 'Other';
    }
